/// Ubuntu design system spacing constants
/// Following Ubuntu's 8px grid system
class UbuntuSpacing {
  UbuntuSpacing._();

  /// 4px - Extra small spacing
  static const double xs = 4.0;

  /// 8px - Small spacing, base grid unit
  static const double sm = 8.0;

  /// 16px - Medium spacing, most common
  static const double md = 16.0;

  /// 24px - Large spacing
  static const double lg = 24.0;

  /// 32px - Extra large spacing
  static const double xl = 32.0;

  /// 48px - Extra extra large spacing
  static const double xxl = 48.0;

  /// 64px - Maximum spacing
  static const double max = 64.0;
}

/// Ubuntu design system border radius constants
class UbuntuRadius {
  UbuntuRadius._();

  /// 4px - Small radius for form controls
  static const double sm = 4.0;

  /// 8px - Medium radius for buttons and cards
  static const double md = 8.0;

  /// 12px - Large radius for prominent cards
  static const double lg = 12.0;

  /// 16px - Extra large radius for modals
  static const double xl = 16.0;
}

/// Ubuntu design system elevation constants
class UbuntuElevation {
  UbuntuElevation._();

  /// No elevation
  static const double none = 0.0;

  /// Subtle elevation for cards
  static const double low = 2.0;

  /// Medium elevation for floating elements
  static const double medium = 4.0;

  /// High elevation for modals and overlays
  static const double high = 8.0;
}
